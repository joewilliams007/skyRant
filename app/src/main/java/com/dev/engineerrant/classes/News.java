package com.dev.engineerrant.classes;

public class News {
    Integer id;
    String type;
    String headline;
    String body;
    String footer;
    Integer height;
    String action;

    public Integer getId() {
        return id;
    }

    public String getType() {
        return type;
    }

    public String getHeadline() {
        return headline;
    }

    public String getBody() {
        return body;
    }

    public String getFooter() {
        return footer;
    }

    public Integer getHeight() {
        return height;
    }

    public String getAction() {
        return action;
    }
}
