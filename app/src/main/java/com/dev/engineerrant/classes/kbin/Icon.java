package com.dev.engineerrant.classes.kbin;

public class Icon {
    String filePath;
    String sourceUrl;
    String storageUrl;
    String altText;
    Integer width;
    Integer height;

    public String getFilePath() {
        return filePath;
    }

    public String getSourceUrl() {
        return sourceUrl;
    }

    public String getStorageUrl() {
        return storageUrl;
    }

    public String getAltText() {
        return altText;
    }

    public Integer getWidth() {
        return width;
    }

    public Integer getHeight() {
        return height;
    }
}
