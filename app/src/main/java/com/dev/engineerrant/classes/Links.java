package com.dev.engineerrant.classes;

public class Links {
    String type;
    String url;
    String short_url;
    String title;
    int start;
    int end;
    int special;

    public String getType() {
        return type;
    }

    public String getUrl() {
        return url;
    }

    public String getShort_url() {
        return short_url;
    }

    public String getTitle() {
        return title;
    }

    public int getStart() {
        return start;
    }

    public int getEnd() {
        return end;
    }

    public int getSpecial() {
        return special;
    }
}
